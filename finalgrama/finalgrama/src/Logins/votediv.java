    private void showbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showbtnActionPerformed
         try{
            model=(DefaultTableModel) jTable2.getModel();
            
            table=new JTable(model);
            

            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/gramadb", "root", "");
            String query = "SELECT * FROM gramaniladhari";
            Statement st = con.createStatement();
            
            PreparedStatement pstmt1 = null;
            pstmt1 = con.prepareStatement(query);
            
            rs = pstmt1.executeQuery();
            Object[] row = new Object[13];
            int r=0;
            int c=0;
            int rn=1;
            while(rs.next()){
               
                model.setRowCount(rn);
                table.setValueAt(rs.getString("GNdivisionnmb"),r,c);
                table.setValueAt(rs.getString("GNdivision"),r,c+1);
                table.setValueAt(rs.getInt("pollingdivision"),r,c+2);
                table.setValueAt(rs.getString("pollingdistrictnmb"),r,c+3);
                table.setValueAt(rs.getInt("electoraldistrict"),r,c+4);
                

                r++;
                rn++;
                c=0;
           }
        }
           catch(Exception ex){
           
                JOptionPane.showMessageDialog(null, ex);
           }
                                               
    }//GEN-LAST:event_showbtnActionPerformed

